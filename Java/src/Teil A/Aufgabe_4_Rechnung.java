class Aufgabe_4_Rechnung
	{
	public static void main(String[]args)
	{
	//Deklaration
	int wert1, wert2;
	int summe, differenz, produkt;
	//Initialisierung
	wert1 = 6;
	wert2 = 3;
	//Berechnung
	summe = wert1 + wert2;
	differenz =	wert1 - wert2;
	produkt = wert1 * wert2;
	//Ausgabe
	System.out.println("Die Berechnung wurde durchgefuehrt!");
	System.out.println(wert1 +"+" + wert2 + "=" + summe);
	System.out.println(wert1 +"-" + wert2 + "=" + differenz);
	System.out.println(wert1 +"*" + wert2 + "=" + produkt);
	}
	}
